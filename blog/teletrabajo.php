 <h2> El teletrabajo: otras características a considerar</h2>
 <figure>
  <img src="imagenes/teletrabajo.jpg" alt="">
</figure>
</div>

A estas alturas, si te has mantenido interesado en el tema y has leído nuestros artículos anteriores en donde hablamos sobre las ventajas del teletrabajo, un poco de su antecedente e indagamos sobre los derechos y obligaciones de quienes participan en esta modalidad. Muy seguramente ya te habrás cansado de ciertos tecnicismos y de hablar sobre reformas.

Por ello, en esta ocasión nos permitimos a platicar contigo de una forma más amena y digerible, abordando otros aspectos y características generales a considerar sobre el teletrabajo, ¿qué te parece?

<h3>Iniciemos por los elementos más agradables </h3>
En la modalidad de trabajo a distancia, podemos encontrar algunas condiciones positivas de modo subjetivo. Dejemos atrás a los beneficios de costos y energía, que si bien son muchos y ya hemos hablado de ellos, ahora podemos centrarnos en la cultura y libertad personal. Por lo que a continuación te presento tan solo tres de las más fundamentales. 

<h3>Vida familiar y personal</h3>
En ocasiones, cuando se trabaja fuera de casa, no es posible mantener un contacto directo con el resto de la familia. Poder tener la oportunidad de interactuar y estar en relación directa con ellos en horarios laborales, es positivo en pro de la unión de las familias y los tiempos compartidos. 

<h3>Estabilidades y despreocupaciones</h3>
El teletrabajo trae consigo veneficios que aportan seguridad para nosotros mismos y nuestras familias. El hecho de que estemos en interacción y cerca a esta, disminuye las preocupaciones e incertidumbres que pueden existir cuando trabajamos fuera de casa y no tenemos contacto con la familia en los horarios laborales.

<h3>Flexibilidad</h3>
 Nuestras vidas personales siempre nos van a pedir tiempos no sólo para relajarse, sino que también para las responsabilidades que se puedan presentar. En este sentido, la versatilidad que tiene el teletrabajo al no establecer horarios, nos permite atender a aquellas otras necesidades que piden atención en prioridad. Todos hemos presentado imprevistos y emergencias familiares, ¿no? 

<h3>¿Qué utilidad tiene conocer esto?</h3>
A estas alturas seguramente te preguntarás qué tan importante es recalcar estas condiciones personales. Pues bien, es bastante importante si consideramos que la construcción de una empresa que aspira a la competitividad digital, pero sobre todo al éxito empresarial, no sólo depende de los conocimientos, habilidades y herramientas de los talentos humanos, sino que también de sus grados de satisfacción y comodidad en la empresa. 

En un ejemplo claro, podemos mencionar a Google, ese gigante corporativo que todos conocemos. Al pensar en su inmensidad, se creería que trabajar en esta empresa podría ser difícil y muy cansado. Todo fuera de la realidad. Desde su principio, Google se ha mantenido constante en el enfoque personal de sus talentos humanos. Comprende que la satisfacción y estabilidad emocional, psicológica y mental son igual de importantes en las condiciones laborales. 

Déjame dejar algo claro: no significa que tomemos las mismas acciones que esta gran empresa, pero sí tomar cartas en la satisfacción y comodidad laboral. ¿Ahora vemos la influencia del teletrabajo en el desarrollo personal, que a su vez impacta en la propia competitividad? 

El teletrabajo hace a las personas más satisfechas con sus labores, por ende, más productivas. 

<h3>Las dificultades existen en todos lados</h3>
Los aspectos positivos no lo son todo. Es evidente de que siempre vamos a toparnos con complicaciones que hagan las tareas un poco más difícil. Es por ello que a continuación podrás leer algunas de ellas. 

Se debilita la comunicación
 Al no tener de frente a las y los compañeros de trabajo, así como a quienes lideran, la comunicación se hace más cortada y distante. Si bien es evidente que tenemos a nuestro alcance las redes sociales, correos electrónicos y plataformas de trabajo, es claro que no es lo mismo hablar de frente a frente que responderse correos todos los días. 

¿Qué tan difícil es sobrellevar esta dificultad? En realidad, no es tan difícil. Esto dependerá de las y los líderes de equipo. Son aquellas personas las responsables de gestionar al equipo ahora digitales y las encargadas de proponer estrategias que faciliten y enriquezcan la comunicación. ¿Estás consciente de ello?

<h3>Las evaluaciones de desempeño se corrompen</h3>
Al tener un trabajo  a distancia, se puede perder la mirada de las y los compañeros y de la propia empresa. Los objetivos del trabajo comienzan a variar debido a la poca interacción y enriquecimiento que pueda haber a distancia. De este modo, el trabajo comienza a cambiar. 

Al igual que la dificultad anterior, esta no es otra que no se pueda superar. Poder trabajar en conjunto desde la modalidad virtual, es una capacidad que se puede mejorar a través del tallereo de las evaluaciones. Las y los líderes de equipo deben de promover el desarrollo de sus equipos y saber atender a este las crisis que surjan, ¿en tu empresa están al tanto de ello? 

<h3>Los problemas técnicos nunca pueden faltar</h3>
¿A cuántos no se nos ha ido el Internet en medio de un día de actividad laboral o nos hemos cansado de estar en una silla para nada cómoda al trabajar? Los problemas externos siempre van a estar presentes para interrumpir nuestras actividades. Ya sea por problemas del Internet, ciertas plataformas que solicitan cuotas o incluso defectos en los propios dispositivos. O también podría ser que no tenemos Internet, dispositivos para trabajar a distancia o cualquier otra herramienta que necesitemos. Es necesario estar conscientes de que estos problemas existen y tarde o temprano se podrían presentar. 

Si bien no necesariamente es un problema causado dentro de la empresa, sí es uno que pueden y deben de atender. ¿Eres consciente de las obligaciones que tienen las empresas para con sus empleados? Es responsabilidad de las empresas con base en la Ley Federal del Trabajo (LFT), atender y proporcionar todas las facilidades que la o el empleado requieran: desde los montos proporcionales de electricidad e internet, hasta los dispositivos y herramientas como computadoras, impresoras e incluso sillas ergonómicas. ¿En tu empresa han atendido a estas disposiciones?

<h3>Finalmente...</h3>
Así como estas condiciones tanto benéficas como las complicaciones, pueden existir muchas más en esta lista para el teletrabajo. Se pueden desarrollar buenos ambientes laborales desde casa y solucionar las complicaciones a distancia teniendo una buena cultura empresarial. ¿Sabes si en tu empresa tienen una buena cultura empresarial o cómo podrían mejorarla? 

En Talents Code, sabemos que es importante para ti y para todos tus colaboradores enriquecer una buena cultura laboral, y así obtener la calidad de trabajo que requieren. 
