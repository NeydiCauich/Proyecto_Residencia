  <p>Trabajar a distancia puede verse fácil y práctico. La verdad es que a nivel general sí lo es. Sin embargo, existen elementos que todos y todas debemos de tener en claro sobre esta práctica. Tanto para las y los empleadores, así como para las y los empleados, ¿sabes cuáles son? Los derechos y obligaciones del teletrabajo. Aquí te los decimos </p>
  <br>
  <h3>Obligaciones de las dos partes</h3>
  <p>Quienes son responsables de liderar las empresas, tienen consideraciones diferentes con sus empleados y empleadas. Tanto en los trabajos convencionales como en el home office. 
<br>
En el teletrabajo, llegan normas específicas para esta modalidad que aseguran y protegen a sus trabajadores y trabajadoras. Es necesario que las conozcas para poder realizar una buena migración y consolidación de esta modalidad en nuestras empresas. </p>
<br>
<h3>¿Por qué es importante esto?</h3>
<p>Debido a que dichas obligaciones no son sólo por compromiso o moral, sino que ya se han establecido normas y políticas para el buen desarrollo del teletrabajo. Hablar sobre estas reformas, no tiene por qué ser tedioso o aburrido, aquí te dejo la información más fundamental para poder conocer las bases de las regulaciones del Gobierno para el teletrabajo. </p>
<br>
<li>En el boletín sobre la Ley Federal del Trabajo (LFT), se dejan clara las delimitaciones de lo que es el teletrabajo, comprendiéndolo como el desarrollo de por lo menos el 40% de las actividades laborales desde casa. </li>
<br>
<li>Los acuerdos entre empleadores o empleadoras y las o los trabajadores deberá de ser especificada explícitamente en los contratos de los mismos. Desde los tiempos y formas, hasta las metodologías a implementar. </li>
<br>
<li>
Las y los empleados deben de gozar de prestaciones y seguridad social en los trabajos presenciales, así como en los trabajos a distancia. 
</li>
<br>
<li>La igualdad entre las y los teletrabajadores debe de mantenerse con respecto a quienes trabajan de forma presencial. </li>
<br>
<li>Derecho a la desconexión digital. Quienes trabajan en la modalidad a distancia, tienen derecho a tener una desconexión con sus labores. Las o los empleadores, deben de respetar la privacidad y los tiempos personales de sus trabajadores y trabajadoras. 
Estas son apenas cinco de los múltiples acuerdos establecidos en la Ley Federal del Trabajo. Tenerlos en prioridad es vital para poder implementar esta modalidad en tu empresa, o bien, para hacer valer tus derechos si eres empleado o empleada de una. </li>
<br>
<h3>¿Conoces cuáles son todos los derechos que debe respetar el teletrabajo? </h3>
<br>
<p>Es importante que sí o sí, todos estemos consciente de ellos, puesto que es la base para el éxito empresarial que pretendemos alcanzar. En Talents Code, somos conscientes de la estabilidad y protección que merecen nuestros colaboradores, por lo que ponemos en práctica las bases que la LFT menciona. 

Es así, que queremos compartir contigo toda esa información detallada, de los derechos y obligaciones de las empresas para con sus empleados, para que los equipos virtuales de trabajo sean de alto desempeño y alcancen el éxito empresarial de forma enriquecida. </p>
